public class MainEjercicio1Nuevo {

    public static void main(String[] args) {
        // Puntuación inicial
        int puntuacionInicial = 0;

        System.out.println("Puntuación inicial: "+puntuacionInicial);

        // Primera modificación
        puntuacionInicial = 5;

        System.out.println("Después de primera modificación: "+puntuacionInicial);

        // Segunda modificación
        puntuacionInicial = 10;

        System.out.println("Después de segunda modificación: "+puntuacionInicial);

        // Puntuación final
        puntuacionInicial = 15;

        System.out.println("Puntuación final: "+puntuacionInicial);
    }
}
